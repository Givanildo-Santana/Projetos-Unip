package aula2;

public class Operacoes {
        
    float valor1;
    float valor2;
    float resultado;
    

public void Somar(){
resultado = valor1 + valor2;
}
public void subtrair(){
resultado = valor1 - valor2;
}
public void multiplicar(){
resultado = valor1 * valor2;
}
public void dividir(){
resultado = valor1 / valor2;
}
}

